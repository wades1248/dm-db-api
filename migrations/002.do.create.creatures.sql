CREATE TABLE creatures (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL,
    source TEXT NOT NULL,
    cr INTEGER NOT NULL,
    xp INTEGER NOT NULL,
    ac INTEGER NOT NULL,
    dexmod INTEGER NOT NULL,
    artic BOOLEAN NOT NULL,
    coastal BOOLEAN NOT NULL, 
    desert BOOLEAN NOT NULL, 
    forest BOOLEAN NOT NULL,
    grasslands BOOLEAN NOT NULL,
    hill BOOLEAN NOT NULL, 
    mountain BOOLEAN NOT NULL, 
    swamp BOOLEAN NOT NULL, 
    underdark BOOLEAN NOT NULL, 
    underWater BOOLEAN NOT NULL,
    urban BOOLEAN NOT NULL
)